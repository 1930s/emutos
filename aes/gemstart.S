.globl _ui_start
.globl _pgmld
.globl _gemdos

.xdef _ad_stail
        .text

//_main:
_ui_start:
        movea.l a7,a5
        movea.l #gemusp,a7
        movea.l 4(a5),a5

        move.l  a5,d0
        add.l   #0x80,d0
        move.l  d0,_ad_stail

        move.l  0xC(a5),d0
        add.l   0x14(a5),d0
        add.l   0x1C(a5),d0
        add.l   #0x100,d0
        move.l  d0,-(a7)
        move.l  a5,-(a7)
        move.w  d0,-(a7)
        move.w  #0x4A,-(a7)
        trap    #1
        adda.l  #0xC,a7

        clr.l  -(a7)
        move.w  #0x20,-(a7)
        trap    #1
        //addq.w  #6,a7
        move.l  d0,old_gem_ssp

        move.l  0x2C(a5),_ad_envrn

        jsr     _size_theglo
        movea.l #_D,a0
        clr.l   d1
        bra     LB4
LB2:
        move.w  d1,(a0)+
LB4:
        dbmi    d0,LB2
        lea     _justretf,a0
        move.l  a0,_drwaddr
        lea     _tikcod,a0
        move.l  a0,_tikaddr
        movea.l #_D,a6
        movea.l a6,a5
        adda.l  #0x74A,a6
        move.l  a6,0x3E(a5)
        movea.l a6,a7
        jsr     _gem_main

        move.l  old_gem_ssp,-(a7)
        move.w  #0x20,-(a7)
        trap    #1
        addq.w  #6,a7

        move.l  #0,-(a7)
        trap    #1




_gemdos:
LF2:
        move.l  (SP)+,saveretaddr

        movem.l d2/a2,saveregs

        move.w  #0,_DOS_ERR
        move.w  #0,_DOS_AX
        trap    #1
        cmp.l   #0,D0
        bge     L134
        move.w  #1,_DOS_ERR
        move.w  D0,_DOS_AX
        cmp.w   #0xFFE0,D0
        bgt     L134
        not.w   _DOS_AX
        subi.w  #0x1E,_DOS_AX
L134:

        movem.l saveregs,d2/a2

        move.l  saveretaddr,-(SP)
        rts




.bss


old_gem_ssp:
        .ds.l   1



saveretaddr:
        .ds.l   1


// Space for the Stack:
        .ds.b   0x100
gemusp:



saveregs:
        .ds.l   2

