/************************************************************************/
/*									*/
/*	Bios initialization.  Must be done before any regular BIOS	*/
/*	calls are performed.						*/
/*									*/
/************************************************************************/
/*
**  xbiosinit - bios initialization.
**	called from xbiosa.s, this routine will do necessary bios initialization
**	that can be done in hi level lang.  biosa.s has the rest.
*/

VOID xbiosinit()
{
	/*
	**  print version string
	*/
    kprint("XBIOS: Initialization ...");
}


/*****************************************************************************
    xbios_null - so lint wont complain
*****************************************************************************/

VOID xbios_null(UWORD x , UWORD y , BYTE * ptr )
{
    x = y ;
    y = x ;
    ++ptr ;
}

